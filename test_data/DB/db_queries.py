SELECT_EMPLOYEE_BADGE = """
WITH RankedBadges AS (
    SELECT 
        BADGE_ID, 
        ROW_NUMBER() OVER (PARTITION BY PERSON_ID ORDER BY END_DATE DESC) AS rn
    FROM 
        RWSUSER.TA_EMP_BADGE 
    WHERE 
        PERSON_ID IN (SELECT PERSON_ID FROM RWSUSER.RWS_EMPLOYEE WHERE EMPLOYEE_ID = '{TEMP}')
)
SELECT BADGE_ID 
FROM RankedBadges
WHERE rn = 1;
"""

